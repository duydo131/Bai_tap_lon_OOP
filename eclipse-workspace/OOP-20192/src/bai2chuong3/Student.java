package bai2chuong3;

public class Student {
	
	private String name;
	private int year;
	
	public Student() {
		
	}
	
	public void setName(String newname) {
		this.name = newname;
	}
	
	public void setYear(int newyear) {
		this.year = newyear;
	}
	
	public String getName() {
		return this.name;
	}
	
	public int getYear() {
		return this.year;
	}
}
